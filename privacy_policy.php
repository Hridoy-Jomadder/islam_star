                    অবস্থানগত তথ্য: যখন আপনি স্টার ব্যবহার করেন, আমরা আপনাকে প্রাসঙ্গিক বিজ্ঞাপন দেখানো সহ আপনার প্রত্যাশিত পরিষেবা প্রদানের জন্য আপনার আনুমানিক অবস্থান সম্পর্কে কিছু তথ্য সংগ্রহ করি। আপনি আপনার অ্যাকাউন্টে এই সেটিংস সক্ষম করে আপনার বর্তমান সুনির্দিষ্ট অবস্থান বা স্থানগুলি যেখানে আপনি পূর্বে স্টার ব্যবহার করেছেন সেগুলি ভাগ করতেও চয়ন করতে পারেন৷

                    অনুমিত পরিচয়: আমরা এমন তথ্য সংগ্রহ বা গ্রহণ করতে পারি যা আমরা আপনার পরিচয় অনুমান করার জন্য ব্যবহার করি নীচে বিশদভাবে:
                    আপনি যখন কোনো ব্রাউজার বা ডিভাইসে স্টার সাইন ইন করবেন, তখন আমরা সেই ব্রাউজার বা ডিভাইসটিকে আপনার অ্যাকাউন্টের সাথে সংযুক্ত করব। আপনার সেটিংস সাপেক্ষে, আমরা আপনার অ্যাকাউন্টকে স্টার-এ সাইন ইন করতে ব্যবহার করা ছাড়া অন্য ব্রাউজার বা ডিভাইসগুলির সাথেও যুক্ত করতে পারি (অথবা আপনার সাইন-আউট করা ডিভাইস বা ব্রাউজারকে অন্যান্য ব্রাউজার বা ডিভাইস বা স্টার-জেনারেটেড শনাক্তকারীর সাথে সংযুক্ত করতে পারি)।
                    যখন আপনি স্টার-কে একটি ইমেল ঠিকানা বা ফোন নম্বর সহ অন্যান্য তথ্য প্রদান করেন, তখন আমরা সেই তথ্যটি আপনার স্টার অ্যাকাউন্টের সাথে যুক্ত করি। আপনার সেটিংস সাপেক্ষে, আমরা আপনার পরিচয় সম্পর্কে অন্যান্য তথ্য অনুমান করার জন্য এই তথ্যটি ব্যবহার করতে পারি, 
                    
                    <!-- উদাহরণস্বরূপ, আপনার অ্যাকাউন্টকে ইমেল ঠিকানাগুলির হ্যাশের সাথে সংযুক্ত করে যা আপনি স্টার-কে দেওয়া ইমেল ঠিকানার সাথে সাধারণ উপাদানগুলি ভাগ করে। -->
                    আপনি যখন স্টার অ্যাক্সেস করেন এবং সাইন ইন করেন না, তখন আমরা সংগ্রহ করা তথ্যের উপর ভিত্তি করে আপনার পরিচয় অনুমান করতে পারি।
                    একটি ল্যাপটপ এবং সেল ফোন বিভিন্ন ধরনের তথ্যের প্রতিনিধিত্বকারী আইকনগুলির পাশাপাশি কম্পিউটার কোডের স্ক্রীন দেখায়৷
                    লগ ইনফরমেশন। আপনি যখন আমাদের পণ্য এবং পরিষেবাগুলিতে সামগ্রী দেখেন বা অন্যথায় যোগাযোগ করেন তখন আমরা তথ্য পেতে পারি, এমনকি যদি আপনি একটি অ্যাকাউন্ট তৈরি না করেন বা সাইন আউট করেন, যেমন:

                    আইপি ঠিকানা এবং সম্পর্কিত তথ্য; ব্রাউজারের ধরন এবং ভাষা; অপারেটিং সিস্টেম; উল্লেখ করা ওয়েবপৃষ্ঠা; অ্যাক্সেস সময়; পরিদর্শন করা পৃষ্ঠাগুলি; অবস্থান; আপনার মোবাইল ক্যারিয়ার; ডিভাইস তথ্য (ডিভাইস এবং অ্যাপ্লিকেশন আইডি সহ); অনুসন্ধান পদ এবং আইডি (কোয়েরি হিসাবে জমা দেওয়া হয়নি সেগুলি সহ); স্টার এ আপনাকে দেখানো বিজ্ঞাপনগুলি; স্টার-জেনারেটেড শনাক্তকারী; এবং কুকিজের সাথে যুক্ত শনাক্তকারী। আপনি যখন স্টার-এর মাধ্যমে অন্য অ্যাপ্লিকেশন ইনস্টল করেন তখন সহ আপনি যখন আমাদের পরিষেবার লিঙ্কগুলিতে ক্লিক করেন, দেখেন বা ইন্টারঅ্যাক্ট করেন তখন আমরা লগ তথ্যও পাই।</h4>
                <h4>1.3 তথ্য আমরা তৃতীয় পক্ষ থেকে প্রাপ্ত,

                    আপনি যখন অন্যান্য অনলাইন পণ্য এবং পরিষেবাগুলি ব্যবহার করেন, তখন তারা সেই ব্যবহার সম্পর্কে তথ্য আমাদের সাথে শেয়ার করতে পারে।

                    বিজ্ঞাপন অংশীদার, বিকাশকারী, প্রকাশক। আমাদের বিজ্ঞাপন এবং ব্যবসায়িক অংশীদাররা আমাদের সাথে তথ্য শেয়ার করে যেমন ব্রাউজার কুকি আইডি, স্টার-জেনারেটেড শনাক্তকারী, মোবাইল ডিভাইস আইডি, হ্যাশ করা ব্যবহারকারীর তথ্য যেমন ইমেল ঠিকানা, ডেমোগ্রাফিক বা আগ্রহের ডেটা এবং ওয়েবসাইট বা অ্যাপে দেখা বা গৃহীত বিষয়বস্তু। আমাদের কিছু বিজ্ঞাপন অংশীদার, বিশেষ করে আমাদের বিজ্ঞাপনদাতারাও আমাদের বিজ্ঞাপন প্রযুক্তিকে একীভূত করে সরাসরি তাদের ওয়েবসাইট বা অ্যাপ থেকে অনুরূপ তথ্য সংগ্রহ করতে সক্ষম করে। বিজ্ঞাপন অংশীদার এবং সহযোগীদের দ্বারা শেয়ার করা তথ্য বা বিজ্ঞাপন অংশীদার এবং সহযোগীদের ওয়েবসাইট এবং অ্যাপ্লিকেশানগুলি থেকে স্টার দ্বারা সংগৃহীত তথ্যগুলি স্টার এর সাথে আপনার শেয়ার করা অন্যান্য তথ্যের সাথে মিলিত হতে পারে এবং স্টার এই গোপনীয়তা নীতিতে অন্য কোথাও বর্ণিত আপনার সম্পর্কে প্রাপ্ত, তৈরি বা অনুমান করে৷

                    অন্যান্য তৃতীয় পক্ষ, অ্যাকাউন্ট সংযোগ এবং ইন্টিগ্রেশন। আমরা তৃতীয় পক্ষের কাছ থেকে আপনার সম্পর্কে তথ্য পেতে পারি যারা আমাদের বিজ্ঞাপন অংশীদার নন, যেমন অন্যান্য স্টার ব্যবহারকারী, বিকাশকারী এবং অংশীদার যারা আমাদের প্ল্যাটফর্ম, আমাদের কর্পোরেট অ্যাফিলিয়েট এবং আপনার লিঙ্ক করা অন্যান্য পরিষেবাগুলিতে সামগ্রীর নিরাপত্তা এবং গুণমান মূল্যায়ন করতে সহায়তা করে আপনার স্টার অ্যাকাউন্ট। আপনি অন্য পরিষেবাতে আপনার অ্যাকাউন্টের সাথে আপনার স্টার অ্যাকাউন্ট সংযোগ করতে বেছে নিতে পারেন, এবং সেই পরিষেবাটি আমাদের কাছে আপনার অ্যাকাউন্ট সম্পর্কে তথ্য পাঠাতে পারে৷</h4>
