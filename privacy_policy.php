                <h4>5.1 অ্যাক্সেস, সংশোধন, বহনযোগ্যতা।<br>

                    আপনি আপনার প্রোফাইল সম্পাদনা করে এবং আপনার অ্যাকাউন্ট সেটিংস সামঞ্জস্য করে আপনার দেওয়া তথ্য অ্যাক্সেস করতে, সংশোধন করতে বা পরিবর্তন করতে পারেন৷

