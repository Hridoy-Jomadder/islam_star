                <h4>3.2 তৃতীয় পক্ষ এবং তৃতীয় পক্ষের ইন্টিগ্রেশন সহ।

                    পরিষেবা প্রদানকারীদের সাথে। আমরা আপনার তথ্য আমাদের পরিষেবা প্রদানকারীদের সাথে শেয়ার করতে পারি যারা আমাদের পক্ষ থেকে কার্য সম্পাদন করে এবং পরিষেবা প্রদান করে, অর্থপ্রদানের সুবিধা প্রদানকারী পেমেন্ট পরিষেবা প্রদানকারী সহ; আমাদের বিভিন্ন ব্লগ এবং উইকি হোস্ট করা পরিষেবা প্রদানকারী; পরিষেবা প্রদানকারী যারা আমাদের পরিষেবার ব্যবহার বুঝতে সাহায্য করে; আবেদনকারী ট্র্যাকিং সিস্টেম প্রদানকারীরা সম্ভাব্য নিয়োগকর্তাদের আবেদনকারী এবং কাজের ডেটা পাঠাতে এবং গ্রহণ করতে; এবং যারা জালিয়াতি সনাক্তকরণ পরিষেবা প্রদান করে।

                    বিজ্ঞাপনদাতাদের সাথে। বিজ্ঞাপনের আয় আমাদের পণ্য এবং পরিষেবা প্রদান করতে সক্ষম করে। বিজ্ঞাপনদাতারা স্টার-তে বা বন্ধ করে তাদের বিজ্ঞাপনের সাথে আপনার ব্যস্ততা থেকে তথ্য শিখতে পারে। উদাহরণস্বরূপ, আপনি যদি আমাদের পরিষেবাগুলিতে একটি বাহ্যিক লিঙ্ক বা বিজ্ঞাপনে ক্লিক করেন, সেই বিজ্ঞাপনদাতা বা ওয়েবসাইট অপারেটর বুঝতে পারেন যে আপনি স্টার থেকে এসেছেন, সাথে সংশ্লিষ্ট অন্যান্য তথ্য সহ আপনি যে বিজ্ঞাপনে ক্লিক করেছেন, যেমন দর্শকদের বৈশিষ্ট্য যা পৌঁছানোর উদ্দেশ্যে ছিল এবং সেই বিজ্ঞাপনের জন্য অন্যান্য স্টার-জেনারেটেড শনাক্তকারী। তারা আপনার কাছ থেকে অন্যান্য ব্যক্তিগত তথ্যও সংগ্রহ করতে পারে, যেমন কুকি শনাক্তকারী বা আপনার আইপি ঠিকানা।

                    তৃতীয় পক্ষের বিষয়বস্তু এবং ইন্টিগ্রেশন। আমরা আপনার সম্মতিতে বা আপনার নির্দেশে আপনার তথ্য শেয়ার করি বা প্রকাশ করি, যেমন আপনি যখন আপনার অ্যাকাউন্ট অ্যাক্সেস করার জন্য তৃতীয় পক্ষের ওয়েব ক্লায়েন্ট বা অ্যাপ্লিকেশন অনুমোদন করেন বা যখন আপনি আমাদেরকে একটি ব্যবসার সাথে আপনার প্রতিক্রিয়া ভাগ করার জন্য নির্দেশ দেন। একইভাবে, আপনার অভিজ্ঞতা উন্নত করতে, আমরা তৃতীয় পক্ষের অংশীদারদের সাথে স্টার-তে তাদের ভিডিও সামগ্রী প্রদর্শন করতে বা ক্রস-প্ল্যাটফর্ম ভাগাভাগি করার অনুমতি দেওয়ার জন্য কাজ করি। আপনি যখন আমাদের ভিডিও বা ক্রস-প্ল্যাটফর্ম শেয়ারিং অংশীদারদের থেকে সামগ্রী দেখেন বা অন্যভাবে ইন্টারঅ্যাক্ট করেন, তখন তারা তাদের গোপনীয়তা নীতিতে বর্ণিত আপনার ব্যক্তিগত তথ্য গ্রহণ এবং প্রক্রিয়া করতে পারে। ভিডিও সামগ্রীর জন্য, আপনি যদি সেই সামগ্রীটি স্বয়ংক্রিয়ভাবে না চালানো পছন্দ করেন তবে আপনি আপনার অটোপ্লে সেটিংস সামঞ্জস্য করতে পারেন৷

                    আমাদের API এর মাধ্যমে। ওয়েবসাইট, অ্যাপ এবং অন্যদের ব্যবহারের জন্য সর্বজনীন স্টার তথ্য উপলব্ধ করতে আমরা API এবং এম্বেডের মতো প্রযুক্তি ব্যবহার করি, উদাহরণস্বরূপ, একটি সংবাদ ওয়েবসাইটে পোস্ট প্রদর্শন করা বা স্টার-এ লোকেরা কী বলে তা বিশ্লেষণ করা। আমরা সাধারণত এই সামগ্রীটি সীমিত পরিমাণে উপলব্ধ করি। বিনামূল্যের জন্য এবং বড় আকারের অ্যাক্সেসের জন্য লাইসেন্সিং ফি চার্জ করুন। আমাদের কাছে স্ট্যান্ডার্ড শর্তাবলী রয়েছে যা এই তথ্যগুলিকে কীভাবে ব্যবহার করা যেতে পারে তা নিয়ন্ত্রণ করে এবং এই শর্তাবলী প্রয়োগ করার জন্য একটি সম্মতি প্রোগ্রাম রয়েছে। কিন্তু এই ব্যক্তি এবং কোম্পানিগুলি স্টার-এর সাথে অনুমোদিত নয়, এবং তাদের অফারগুলি স্টার-এ আপনার করা আপডেটগুলিকে প্রতিফলিত নাও করতে পারে৷ আমরা কীভাবে স্টার-এ সর্বজনীন ডেটা বিশ্বের কাছে উপলব্ধ করি সে সম্পর্কে আরও তথ্যের জন্য, https://developer.star.com এ যান৷</h4>
                <h4>3.3 যখন আইন দ্বারা প্রয়োজন হয়, ক্ষতি প্রতিরোধ করতে, বা জনস্বার্থে।

                    আমরা আপনার তথ্য সংরক্ষণ, ব্যবহার, ভাগ বা প্রকাশ করতে পারি যদি আমরা বিশ্বাস করি যে এটি করা যুক্তিসঙ্গতভাবে প্রয়োজনীয়:

                    একটি আইন, প্রবিধান, আইনি প্রক্রিয়া, বা সরকারী অনুরোধ মেনে চলা;

                    আমাদের পরিষেবাগুলিতে স্প্যাম, অপব্যবহার, বা দূষিত অভিনেতা প্রতিরোধে সহায়তা সহ যে কোনও ব্যক্তির সুরক্ষা, আমাদের প্ল্যাটফর্মের সুরক্ষা বা অখণ্ডতা রক্ষা করা;

                    ব্যাখ্যা করুন কেন আমরা আমাদের পরিষেবাগুলি থেকে সামগ্রী বা অ্যাকাউন্টগুলি সরিয়েছি (যেমন, আমাদের নিয়ম লঙ্ঘনের জন্য);

                    জালিয়াতি, নিরাপত্তা, বা প্রযুক্তিগত সমস্যা সমাধান; বা

                    আমাদের অধিকার বা সম্পত্তি, বা যারা আমাদের পরিষেবা ব্যবহার করেন তাদের অধিকার বা সম্পত্তি রক্ষা করুন।</h4>
                <h4>3.4 আমাদের অ্যাফিলিয়েটদের সাথে।

                    আমরা আমাদের পণ্য এবং পরিষেবা প্রদানের জন্য আমাদের সহযোগীদের মধ্যে তথ্য শেয়ার করতে পারি।</h4>
                <h4>3.5 মালিকানা পরিবর্তনের ফলে।

                    আমরা একীভূতকরণ, অধিগ্রহণ, পুনর্গঠন, সম্পদ বিক্রি বা দেউলিয়া হওয়ার সাথে সম্পর্কিত আপনার সম্পর্কে তথ্য শেয়ার, বিক্রয় বা স্থানান্তর করতে পারি। এই গোপনীয়তা নীতি আপনার ব্যক্তিগত তথ্যের ক্ষেত্রে প্রযোজ্য হবে যা শেয়ার করা হয়েছে (যেকোনো লেনদেনের আগে এবং পরে) বা নতুন সত্তার কাছে স্থানান্তর করা হয়েছে।
                </h4>
                <h4>আপনি কতক্ষণ ডেটা রাখেন?<br>
                    সাধারণত, যতক্ষণ আপনার অ্যাকাউন্ট থাকে ততক্ষণ আমরা আপনার ডেটা রাখি।

                    4. আমরা কতক্ষণ তথ্য রাখি

                    আমরা বিভিন্ন সময়ের জন্য বিভিন্ন ধরনের তথ্য রাখি:

                    আমরা আপনার অ্যাকাউন্টের সময়কালের জন্য আপনার প্রোফাইল তথ্য এবং সামগ্রী রাখি।

                    আপনি যখন সর্বোচ্চ 18 মাসের জন্য আমাদের পণ্য এবং পরিষেবাগুলি ব্যবহার করেন তখন আমরা সাধারণত অন্যান্য ব্যক্তিগতভাবে শনাক্তযোগ্য ডেটা রাখি যা আমরা সংগ্রহ করি।

                    মনে রাখবেন যে সর্বজনীন বিষয়বস্তু আপনি স্টার থেকে মুছে ফেলার পরেও অন্য কোথাও থাকতে পারে৷ উদাহরণস্বরূপ, সার্চ ইঞ্জিন এবং অন্যান্য তৃতীয় পক্ষগুলি তাদের নিজস্ব গোপনীয়তা নীতির উপর ভিত্তি করে আপনার পোস্টগুলির অনুলিপিগুলিকে দীর্ঘক্ষণ ধরে রাখতে পারে, এমনকি সেগুলি স্টার-তে মুছে ফেলা বা মেয়াদ শেষ হওয়ার পরেও৷

                    যেখানে আপনি আমাদের নিয়ম লঙ্ঘন করেন এবং আপনার অ্যাকাউন্ট স্থগিত করা হয়, সেখানে আমরা অ্যাকাউন্ট তৈরি করতে আপনার ব্যবহৃত শনাক্তকারীগুলিকে (যেমন, ইমেল ঠিকানা বা ফোন নম্বর) অনির্দিষ্টকালের জন্য রাখতে পারি যাতে পুনরাবৃত্তি নীতি অপরাধীদের নতুন অ্যাকাউন্ট তৈরি করা থেকে বিরত থাকে।

                    আইনি প্রয়োজনীয়তা মেনে চলার জন্য এবং নিরাপত্তা ও নিরাপত্তার কারণে আমরা নির্দিষ্ট কিছু তথ্য আমাদের নীতিমালার থেকে বেশি সময় ধরে রাখতে পারি।
                </h4>
                <h4>আমি কিভাবে আমার ডেটা নিয়ন্ত্রণ করতে পারি?
                    আপনি এটি অ্যাক্সেস করতে, এটি মুছে ফেলতে বা আপনার সেটিংস পরিবর্তন করতে পারেন৷ মূলত, আপনি বস।
